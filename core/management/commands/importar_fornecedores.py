EMAIL_PADRAO = 'alberto_rosa10@icloud.com'
SENHA_PADRAO = '12345678'


class Command(BaseCommand):
    help = 'Importa fornecedores reais da AG Simões como Fornecedor + User'

    def handle(self, *args, **options):
        criados = 0
        pulados = 0
        erros   = 0

        for razao_social, cnpj_raw in FORNECEDORES:
            import re
            cnpj = re.sub(r'\D', '', cnpj_raw)

            try:
                user_existe       = User.objects.filter(username=cnpj).exists()
                fornecedor_existe = Fornecedor.objects.filter(cnpj=cnpj).exists()

                if user_existe or fornecedor_existe:
                    self.stdout.write(f'  ⏭  Pulando {cnpj} — {razao_social[:40]}')
                    pulados += 1
                    continue

                user = User.objects.create_user(
                    username=cnpj,
                    email=EMAIL_PADRAO,
                    password=SENHA_PADRAO,
                )
                Fornecedor.objects.create(
                    user               = user,
                    cnpj               = cnpj,
                    razao_social       = razao_social,
                    email_contato      = EMAIL_PADRAO,
                    score_pontualidade = 100.0,
                    bloqueado          = False,
                    agenda_fixa        = False,
                )
                self.stdout.write(self.style.SUCCESS(f'  ✅ Criado  {cnpj} — {razao_social[:40]}'))
                criados += 1

            except Exception as e:
                self.stdout.write(self.style.ERROR(f'  ❌ Erro    {cnpj} — {razao_social[:40]}: {e}'))
                erros += 1

        self.stdout.write('')
        self.stdout.write('─' * 55)
        self.stdout.write(f'  Total na lista : {len(FORNECEDORES)}')
        self.stdout.write(self.style.SUCCESS(f'  Criados        : {criados}'))
        self.stdout.write(f'  Pulados        : {pulados}')
        self.stdout.write(self.style.ERROR(f'  Erros          : {erros}') if erros else f'  Erros          : {erros}')
        self.stdout.write('─' * 55)
